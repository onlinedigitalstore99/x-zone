## URL Shortner
site_stat() {
    [[ ${1} != "" ]] && curl -s -o "/dev/null" -w "%{http_code}" "${1}https://github.com"
}

shorten() {
    short=$(curl --silent --insecure --fail --retry-connrefused --retry 2 --retry-delay 2 "$1")
    processed_url=${short}
}

custom_url() {
    url=${1#http*//}
    tinyurl="https://tinyurl.com/api-create.php?url="

    if [[ ${url} =~ [-a-zA-Z0-9.]*(trycloudflare.com|loclx.io) ]]; then
        if [[ $(site_stat $tinyurl) == 2* ]]; then
            shorten "$tinyurl$url"
        else
            url="Unable to generate links. Try after turning on hotspot"
            processed_url="Unable to Short URL"
        fi

        url="https://$url"
        processed_url="https://$processed_url"
    else
        url="Unable to generate links. Try after turning on hotspot"
        processed_url="Unable to Short URL"
    fi

    echo -e "\n${RED}[${WHITE}-${RED}]${BLUE} URL 1 : ${GREEN}$url"
    echo -e "\n${RED}[${WHITE}-${RED}]${BLUE} URL 2 : ${PINK}$processed_url"
}



